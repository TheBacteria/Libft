#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	unsigned char	*str;
	size_t			i;
	size_t			l;

	l = size * count;
	if (size != 0 && l / size != count)
		return (NULL);
	i = 0;
	str = (unsigned char *) malloc(count * size);
	if (str == NULL)
		return (NULL);
	while (i < count * size)
	{
		str[i] = 0;
		i++;
	}
	return (str);
}
